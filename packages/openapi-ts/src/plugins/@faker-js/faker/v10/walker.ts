import type { SymbolMeta } from '@hey-api/codegen-core';
import { fromRef } from '@hey-api/codegen-core';
import type { IR, SchemaExtractor, SchemaVisitor } from '@hey-api/shared';
import { childContext, pathToJsonPointer } from '@hey-api/shared';

import { $ } from '../../../../ts-dsl';
import { createFakerWalkerContext } from '../shared/faker-expr';
import type { ProcessorContext } from '../shared/processor';
import type { EmitTracking, Expression, FakerResult, FakerWalkerContext } from '../shared/types';
import type { FakerJsFakerPlugin } from '../types';
import { arrayToExpression } from './toAst/array';
import { booleanToExpression } from './toAst/boolean';
import { enumToExpression } from './toAst/enum';
import { propertyNameFromPath } from './toAst/nameRules';
import { numberToExpression } from './toAst/number';
import { objectToExpression } from './toAst/object';
import { stringToExpression } from './toAst/string';

export interface VisitorConfig {
  /** Set of JSON pointers involved in circular references. */
  circularPointers: Set<string>;
  /** Whether the current top-level schema is itself circular. */
  isCircularSchema: boolean;
  plugin: FakerJsFakerPlugin['Instance'];
  /** Optional schema extractor function. */
  schemaExtractor?: SchemaExtractor<ProcessorContext>;
  /** Shared tracking for conditional helper emission. */
  tracking: EmitTracking;
}

const FALLBACK_UNDEFINED: FakerResult = {
  expression: $('undefined'),
  isObjectLike: false,
  usesAccessor: false,
  usesFaker: false,
};

/**
 * Detect discriminator constraint schemas: objects where every property has a `const` value.
 * These are generated by the IR parser for discriminated unions.
 */
function isDiscriminatorConstraint(schema: IR.SchemaObject): boolean {
  if (schema.type !== 'object' || !schema.properties) return false;
  const props = schema.properties;
  return Object.keys(props).every((key) => props[key]!.const !== undefined);
}

export function createVisitor(
  config: VisitorConfig,
): SchemaVisitor<FakerResult, FakerJsFakerPlugin['Instance']> {
  const { circularPointers, isCircularSchema, plugin, schemaExtractor, tracking } = config;
  const fakerCtx: FakerWalkerContext = createFakerWalkerContext(
    tracking,
    circularPointers,
    isCircularSchema,
    plugin.config.nameRules,
  );

  return {
    applyModifiers(result) {
      return result;
    },
    array(schema, ctx, walk) {
      const result = arrayToExpression({ fakerCtx, schema, walk, walkerCtx: ctx });
      return {
        expression: result.expression,
        hasCircularRef: result.hasCircularRef,
        isObjectLike: false,
        usesAccessor: result.usesFaker,
        usesFaker: result.usesFaker,
      };
    },
    boolean() {
      return {
        expression: booleanToExpression(fakerCtx),
        isObjectLike: false,
        usesAccessor: true,
        usesFaker: true,
      };
    },
    enum(schema) {
      const expression = enumToExpression(fakerCtx, schema);
      // Count valid enum members — only multi-member enums use faker.helpers.arrayElement.
      // Single-member enums (e.g. OpenAPI 3.0 const via `enum: [value]`) resolve to a
      // plain literal and don't need faker.
      const memberCount =
        schema.items?.filter((item) => item.const !== undefined || item.type === 'null').length ??
        0;
      const needsFaker = memberCount > 1;
      return {
        expression,
        isObjectLike: false,
        usesAccessor: needsFaker,
        usesFaker: needsFaker,
      };
    },
    integer(schema, ctx) {
      const nameInfo = propertyNameFromPath(fromRef(ctx.path));
      return {
        expression: numberToExpression(fakerCtx, schema, nameInfo),
        isObjectLike: false,
        usesAccessor: true,
        usesFaker: true,
      };
    },
    intercept(schema, ctx, walk) {
      // const values are always emitted as literals
      if (schema.const !== undefined) {
        return {
          expression: $.fromValue(schema.const),
          isObjectLike: typeof schema.const === 'object' && schema.const !== null,
          usesAccessor: false,
          usesFaker: false,
        };
      }

      if (schemaExtractor && !schema.$ref) {
        const extracted = schemaExtractor({
          meta: {
            resource: 'definition',
            resourceId: pathToJsonPointer(fromRef(ctx.path)),
          },
          naming: ctx.plugin.config.definitions,
          path: fromRef(ctx.path),
          plugin: ctx.plugin,
          schema,
        });

        if (extracted !== schema) {
          return walk(extracted, ctx);
        }
      }
    },
    intersection(items, schemas) {
      if (items.length === 0) {
        return FALLBACK_UNDEFINED;
      }
      if (items.length === 1 && items[0]) {
        return items[0];
      }

      // Check if all items produce object-like values that can be spread.
      // Uses the walked results rather than schema structure, since $ref and
      // union schemas may resolve to non-object types at runtime.
      const allSpreadable = items.every((item) => item.isObjectLike);

      if (!allSpreadable) {
        // Detect impossible intersections where types can never be satisfied.
        // Collect the distinct "kind" of each schema: primitive type name, 'object' for objects/$refs,
        // or 'composite' for nested logical operators.
        const kinds = new Set(
          schemas.map((s) => {
            if (s.$ref || s.type === 'object') {
              return 'object';
            }
            if (s.items && s.logicalOperator) {
              return 'composite';
            }
            return s.type; // primitive type: 'string', 'number', 'boolean', 'array'
          }),
        );
        // More than one kind means incompatible types (e.g. string & object, number & string)
        if (kinds.size > 1) {
          return {
            ...FALLBACK_UNDEFINED,
            resultType: 'never',
          };
        }
        return items[0]!;
      }

      // Reorder so discriminator constraint objects (all-const properties) are spread last
      const indexed = items.map((item, i) => ({ item, schema: schemas[i]! }));
      const regular: typeof indexed = [];
      const discriminators: typeof indexed = [];

      for (const entry of indexed) {
        if (isDiscriminatorConstraint(entry.schema)) {
          discriminators.push(entry);
        } else {
          regular.push(entry);
        }
      }

      // Use Object.assign({}, ...items) instead of spread to merge regular items
      const assignCall = $('Object')
        .attr('assign')
        .call($.object(), ...regular.map(({ item }) => item.expression));

      let expression: Expression;
      if (discriminators.length > 0) {
        // Wrap with spread and emit discriminator constraint properties with `as const`
        const obj = $.object().pretty();
        obj.spread(assignCall);
        for (const { schema } of discriminators) {
          for (const key of Object.keys(schema.properties!)) {
            obj.prop(key, $($.fromValue(schema.properties![key]!.const)).as('const'));
          }
        }
        expression = obj;
      } else {
        expression = assignCall;
      }

      return {
        expression,
        hasCircularRef: items.some((item) => item.hasCircularRef),
        isObjectLike: true,
        usesAccessor: items.some((item) => item.usesAccessor),
        usesFaker: items.some((item) => item.usesFaker),
      };
    },
    never() {
      // Skip never expression
      return {
        ...FALLBACK_UNDEFINED,
        resultType: 'never',
      };
    },
    null() {
      return {
        expression: $.fromValue(null),
        isObjectLike: false,
        usesAccessor: false,
        usesFaker: false,
      };
    },
    number(schema, ctx) {
      const nameInfo = propertyNameFromPath(fromRef(ctx.path));
      return {
        expression: numberToExpression(fakerCtx, schema, nameInfo),
        isObjectLike: false,
        usesAccessor: true,
        usesFaker: true,
      };
    },
    object(schema, ctx, walk) {
      return objectToExpression({ fakerCtx, schema, walk, walkerCtx: ctx });
    },
    postProcess(result, schema) {
      // resolveCondition(options?.useDefault ?? false, f) ? <default> : <faker>
      if (schema.default !== undefined && result.usesFaker) {
        tracking.needsResolveCondition = true;
        const condition = $('resolveCondition').call(
          $.binary($(fakerCtx.optionsId).attr('useDefault').optional(), '??', $.literal(false)),
          fakerCtx.fakerAccessor,
        );
        const defaultExpr = $.fromValue(schema.default);
        return {
          expression: $.ternary(condition).do(defaultExpr).otherwise(result.expression),
          hasCircularRef: result.hasCircularRef,
          usesAccessor: true,
          usesFaker: true,
        };
      }
      return result;
    },
    reference($ref, _schema, ctx) {
      const query: SymbolMeta = {
        category: 'schema',
        resource: 'definition',
        resourceId: $ref,
        tool: 'faker',
      };

      const refSymbol = ctx.plugin.referenceSymbol(query);
      // Only emit depth-aware call sites when the enclosing schema is itself
      // circular — `_callDepth` is only declared as a parameter for circular
      // schemas.  Non-circular schemas that reference a circular one just call
      // it normally; the circular schema starts its own depth tracking from 0.
      const isCircularRef = fakerCtx.isCircularSchema && fakerCtx.circularPointers.has($ref);
      // Look up whether the referenced function accepts options.
      // Schemas are walked in dependency order, so the target is already processed.
      // For circular references (A→B→A or A→A) the target may not be in the map
      // yet; we fall back to true which is safe since options is always optional.
      const refUsesFaker = fakerCtx.tracking.usesFakerByRef.get($ref) ?? true;
      // Look up whether the referenced function returns an object-like value.
      // For circular references, fall back to true (circular schemas are typically objects).
      const refIsObjectLike = fakerCtx.tracking.isObjectByRef.get($ref) ?? true;

      return {
        expression: isCircularRef
          ? $(refSymbol).call(fakerCtx.optionsId, $('_callDepth').plus($.literal(1)))
          : refUsesFaker
            ? $(refSymbol).call(fakerCtx.optionsId)
            : $(refSymbol).call(),
        hasCircularRef: isCircularRef,
        isObjectLike: refIsObjectLike,
        usesAccessor: false,
        usesFaker: refUsesFaker,
      };
    },
    string(schema, ctx) {
      const nameInfo = propertyNameFromPath(fromRef(ctx.path));
      return {
        expression: stringToExpression(fakerCtx, schema, nameInfo),
        isObjectLike: false,
        usesAccessor: true,
        usesFaker: true,
      };
    },
    tuple(schema, ctx, walk) {
      const elements: Array<Expression> = [];
      let anyChildUsesFaker = false;
      let anyChildUsesAccessor = false;
      let anyChildHasCircularRef = false;
      for (let i = 0; i < (schema.items?.length ?? 0); i++) {
        const item = schema.items![i]!;
        const result = walk(item, childContext(ctx, 'items', i));
        if (result.resultType === 'never') {
          return {
            ...FALLBACK_UNDEFINED,
            resultType: 'never' as const,
          };
        }
        elements.push(result.expression);
        if (result.usesFaker) anyChildUsesFaker = true;
        if (result.usesAccessor) anyChildUsesAccessor = true;
        if (result.hasCircularRef) anyChildHasCircularRef = true;
      }
      return {
        expression: $.array(...elements),
        hasCircularRef: anyChildHasCircularRef,
        isObjectLike: false,
        usesAccessor: anyChildUsesAccessor,
        usesFaker: anyChildUsesFaker,
      };
    },
    undefined() {
      return FALLBACK_UNDEFINED;
    },
    union(items, schemas) {
      // Discard impossible branches (e.g. allOf that resolved to never)
      const viableItems = items.filter((item) => item.resultType !== 'never');

      if (viableItems.length === 0) {
        return {
          ...FALLBACK_UNDEFINED,
          resultType: 'never',
        };
      }
      if (viableItems.length === 1 && viableItems[0]) {
        return viableItems[0];
      }

      const viableSchemas = schemas.filter((_, i) => items[i]?.resultType !== 'never');
      const nullIndex = viableSchemas.findIndex((s) => s.type === 'null');
      const hasNull = nullIndex !== -1;
      const nonNullItems = hasNull ? viableItems.filter((_, i) => i !== nullIndex) : viableItems;

      if (nonNullItems.length === 0) {
        return {
          expression: $.fromValue(null),
          isObjectLike: false,
          usesAccessor: false,
          usesFaker: false,
        };
      }

      // Nullable union with exactly one non-null variant: simple ternary
      if (hasNull && nonNullItems.length === 1) {
        let expression = $.ternary(fakerCtx.fakerAccessor.attr('datatype').attr('boolean').call())
          .do(nonNullItems[0]!.expression)
          .otherwise($.fromValue(null));

        // Depth guard: _callDepth > MAX_CALL_DEPTH ? null : (ternary)
        if (nonNullItems[0]!.hasCircularRef) {
          fakerCtx.tracking.needsMaxCallDepth = true;
          expression = $.ternary($('_callDepth').gt($('MAX_CALL_DEPTH')))
            .do($.fromValue(null))
            .otherwise(expression);
        }

        return {
          expression,
          hasCircularRef: nonNullItems[0]!.hasCircularRef,
          isObjectLike: nonNullItems[0]!.isObjectLike,
          usesAccessor: true,
          usesFaker: true,
        };
      }

      const possibleValues = nonNullItems.map((item) => item.expression);
      if (hasNull) {
        possibleValues.push($.fromValue(null));
      }

      // faker.helpers.arrayElement([v1(), v2(), ...])
      let expression: Expression = $(
        fakerCtx.fakerAccessor
          .attr('helpers')
          .attr('arrayElement')
          .call($.array(...possibleValues)),
      );

      const hasCircularRef = items.some((item) => item.hasCircularRef);

      // Depth guard for nullable unions with circular refs
      if (hasNull && hasCircularRef) {
        fakerCtx.tracking.needsMaxCallDepth = true;
        expression = $.ternary($('_callDepth').gt($('MAX_CALL_DEPTH')))
          .do($.fromValue(null))
          .otherwise(expression);
      }

      return {
        expression,
        hasCircularRef,
        // Union is object-like only if all non-null items are object-like
        isObjectLike: nonNullItems.every((item) => item.isObjectLike),
        usesAccessor: true,
        usesFaker: true,
      };
    },
    unknown() {
      return {
        ...FALLBACK_UNDEFINED,
        resultType: 'unknown',
      };
    },

    void() {
      return FALLBACK_UNDEFINED;
    },
  };
}
